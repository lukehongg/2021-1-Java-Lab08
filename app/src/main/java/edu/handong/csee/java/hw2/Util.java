package edu.handong.csee.java.hw2;
/**
 * this class converts input measures to uppercases so that 
 * the system  can easily find which classes to call
 */
public class Util {
    /**
     * 
     * @param stringValue this is given measures
     * @return it returns a string with uppercases
     */
    static String getUppercaseString(String stringValue) {
        return stringValue.toUpperCase();
    }
    
}
